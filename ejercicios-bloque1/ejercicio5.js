/*Ejercicio5:
Tengo una tarta de queso con 16 porciones.
Cada porción vale 3.8 euros.
¿Si vendo todas las porciones, cuanto dinero gano en total?*/


const porciones = 16;
let precio = 3.8;

let precioPorTarta = porciones * precio;

console.log(`Ganaras ${precioPorTarta} euros por tarta de queso vendida`);
