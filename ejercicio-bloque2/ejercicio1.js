/*Ejercicio 1: 

    Nuestros usuarios tienen perros.

    Y quieren saber que edad de perro tienen sus mascotas.

    Un año humano equivale a siete de perro

    Pregunta a los usuarios que edad tiene su perro y dile la edad canina
*/

let edadPerroHumana = prompt("¿Que edad tiene su perro? (en tiempo humano)");

let añoPerro = 7;

let edadPerroCanino = edadPerroHumana * añoPerro;

alert(`Su perro con ${edadPerroHumana} años humanos tiene la edad de ${edadPerroCanino} caninos`);





